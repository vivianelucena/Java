package Aula01;

public class OperadorExemplo3 {
	public static void main(String[] args) {
		int a = 10;
		int b = -10;
		boolean c = true;
		boolean d = false;
		
		System.out.println(~a); 	//	Inverte o sinal e adiciona 1
		System.out.println(~b); 	//	Inverte o sinal e subtrai 1
		System.out.println(!c);		//	False (Oposto ao valor booleano)
		System.out.println(!d);		//	True
	}

}
