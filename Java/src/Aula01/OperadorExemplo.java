package Aula01;

public class OperadorExemplo {
	public static void main(String[] args) {
		int x = 10;
		
		System.out.println(x++); //11 printa o valor de antes, mas acrescenta 0 +1
		System.out.println(x); //11
		System.out.println(++x); //12
		System.out.println(x--); //12 printa o valor de antes, mas acrescenta 0 -1
		System.out.println(--x); //10
		
	}

}
